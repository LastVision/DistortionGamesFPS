	template<typename T>
	void PrintEndl(const T& aSomethingToPrint, eCOLOR aColour)
	{
		SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), WORD(aColour));
		std::cout << aSomethingToPrint << "\n";
		SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), 7); //Colour reset
	}
	template<typename T>
	void Print(const T& aSomethingToPrint, eCOLOR aColour)
	{
		SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), WORD(aColour));
		std::cout << aSomethingToPrint;
		SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), 7); //Colour reset
	}
